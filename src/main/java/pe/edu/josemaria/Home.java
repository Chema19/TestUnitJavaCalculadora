package pe.edu.josemaria;

public class Home {

	public double suma(double a, double b) {
		return a+b;
	}
	
	public double restar(double a, double b) {
		return a-b;
	}
	
	public double dividir(double a, double b) {
		return a/b;
	}
	
	public double multiplivar(double a, double b) {
		return a*b;
	}
}
